      <h3>What is medical waste?</h3>
      <p>Medical wastes, also known as biomedical wastes, consist of  solid or any semi-solid wastes that are produced in hospitals, medical centres,  healthcare facilities, laboratories, etc. These wastes are potentially  infectious and dangerous to human and animal health, as well as to the  environment.<br>
        The  following are some examples of biomedical waste:</p>
      <ul>
        <li>Anatomical waste and solid waste </li>
        <li>Dental amalgam </li>
        <li>Used gauze, gloves, cultures, swabs, bandages, </li>
