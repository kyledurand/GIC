        <h3 id="Sharps">What are Sharps Wastes?</h3>
        <p>Any object that can potentially pierce or scrape human or  animal skin. These are commonly found and produced in medical and healthcare  facilities. These can be made of metal, glass, or plastic. <br>
        Sharps include,  but not limited to the following:</p>
        <ul>
          <li>Needles and syringes </li>
          <li>Lancets and scalpels </li>
          <li>Broken glass </li>
          <li>Culture slides and dishes </li>
          <li>Broken capillary tubes </li>
          <li>Broken rigid plastic </li>
          <li>Exposed ends of dental wires </li>
          <li>Laboratory slides </li>
          <li>Cover slips contaminated with infectious agents</li>
        </ul>
        <h3 id="Biomedical">What are Biomedical Wastes?</h3>
        <p>These are wastes that can potentially harm and cause  diseases. These hazardous wastes are usually generated inside hospitals and  medical facilities.</p>
        <h3 id="Chemical">What are Chemical Wastes?</h3>
        <p>These unsafe  chemicals are outdated, contaminated, unclassified, or leaky. Chemical wastes can  be both hazardous and non-hazardous. The following are some examples of  chemical waste:</p>
        <ul>
          <li>Disinfecting and cleaning agents</li>
          <li>Reagents, acids, and bases</li>
          <li>Varnish strippers, paint thinners, paints, dyes,  and inks</li>
          <li>Solvents, aerosols, and other unknown chemicals</li>
        </ul>
